package sistemacoil.modelo.pojo;

public class NumeraliaAreaAcademica {
    private String areaAcademica;
    private int numeroDeColaboraciones;
    private int numeroDeProfesores;
    private int numeroDeEstudiantes;

    public String getAreaAcademica() {
        return areaAcademica;
    }

    public void setAreaAcademica(String areaAcademica) {
        this.areaAcademica = areaAcademica;
    }

    public int getNumeroDeColaboraciones() {
        return numeroDeColaboraciones;
    }

    public void setNumeroDeColaboraciones(int numeroDeColaboraciones) {
        this.numeroDeColaboraciones = numeroDeColaboraciones;
    }

    public int getNumeroDeProfesores() {
        return numeroDeProfesores;
    }

    public void setNumeroDeProfesores(int numeroDeProfesores) {
        this.numeroDeProfesores = numeroDeProfesores;
    }

    public int getNumeroDeEstudiantes() {
        return numeroDeEstudiantes;
    }

    public void setNumeroDeEstudiantes(int numeroDeEstudiantes) {
        this.numeroDeEstudiantes = numeroDeEstudiantes;
    }
}

